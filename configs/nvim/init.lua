:augroup END

" Set bash-ish tab completion (remove full option for bash-like behavior)
set wildmode=longest,list,full
set wildmenu

" Set yaml spacing
autocmd FileType yaml setlocal ts=2 sts=2 sw=2 expandtab

" Setup persistent undo
set undofile " Maintain undo history between sessions
set undodir=~/.nvim/undodir " Store files needed for persistent undo here

]])

-- Install packer for plugins
local fn = vim.fn
local install_path = fn.stdpath('data')..'/site/pack/packer/start/packer.nvim'
if fn.empty(fn.glob(install_path)) > 0 then
  packer_bootstrap = fn.system({'git', 'clone', '--depth', '1', 'https://github.com/wbthomason/packer.nvim', install_path})
end

-- Setup plugins
require('packer').startup(function(use)
    -- Packer can manage itself
    use 'wbthomason/packer.nvim'

	-- LSP
	use {
	  'VonHeikemen/lsp-zero.nvim',
	  requires = {
	    -- LSP Support
	    {'neovim/nvim-lspconfig'},
	    {'williamboman/nvim-lsp-installer'},

	    -- Autocompletion
	    {'hrsh7th/nvim-cmp'},
	    {'hrsh7th/cmp-buffer'},
	    {'hrsh7th/cmp-path'},
	    {'saadparwaiz1/cmp_luasnip'},
	    {'hrsh7th/cmp-nvim-lsp'},
	    {'hrsh7th/cmp-nvim-lua'},

	    -- Snippets
	    {'L3MON4D3/LuaSnip'},
	    {'rafamadriz/friendly-snippets'},
	  }
	}

    -- Store last position when closing a file
    use 'farmergreg/vim-lastplace'

    -- Boostrap packer 
    if packer_bootstrap then
        require('packer').sync()
    end

end)

